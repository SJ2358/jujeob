    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = idTextField.getText();
        String pw = pwTextField.getText();
        if(id.equals("") || pw.equals("")){
            JOptionPane.showMessageDialog(this, "아이디와 비밀번호를 입력해주세요", "로그인 실패", 1);
            return;
        } // 빈칸으로 입력시 리턴
        try {
            DBM.dbOpen();
            String sql_query = String.format("SELECT memberName, memberPw FROM member WHERE memberId = '%s'",id);
            DBM.DB_rs = DBM.DB_stmt.executeQuery(sql_query);
            if(DBM.DB_rs.next()){
                user.setUserName(DBM.DB_rs.getString(1));
                if (pw.equals(DBM.DB_rs.getString(2))) {
                    JOptionPane.showMessageDialog(this, user.getUserName() + "님 환영합니다", "로그인 성공", 1);
                    
                    Main j = new Main( user.getUserName());
                    j.setVisible(true);
                    this.dispose();
                }
            }else{
                JOptionPane.showMessageDialog(this, "아이디와 비밀번호를 다시 확인해주세요", "로그인 실패", 1);
            }
            DBM.DB_rs.close();
            DBM.dbClose();
        }catch(Exception e){
            System.out.println("SQL Exception : "+ e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

